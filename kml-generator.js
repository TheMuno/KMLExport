function buildKml(tripData) {
  const tripMonth = formatMonthYear(tripData.tripDates.start);
  const docTitle = `${tripData.userName}'s NYC Trip - ${tripMonth}`;

  const styleBlocks = buildStyleBlocks();
  const hotelPlacemark = tripData.hotel ? buildHotelPlacemark(tripData.hotel) : '';
  const dayFolders = tripData.days.map(day => buildDayFolder(day, tripData.tripDates.start)).join('\n');

  return `<?xml version="1.0" encoding="UTF-8"?>
<kml xmlns="http://www.opengis.net/kml/2.2">
<Document>
  <name>${escapeXml(docTitle)}</name>
  <description>Generated by Khonsu Day Planner</description>
  ${styleBlocks}
  ${hotelPlacemark}
  ${dayFolders}
</Document>
</kml>`;
}

function buildStyleBlocks() {
  const types = ['restaurant', 'attraction', 'hotel', 'airport', 'shopping', 'park', 'default'];
  return types.map(type => `
  <Style id="${type}">
    <IconStyle>
      <color>${getKmlColor()}</color>
      <scale>1.0</scale>
      <Icon><href>${getIconUrl(type)}</href></Icon>
    </IconStyle>
  </Style>`).join('\n');
}

function buildDayFolder(day, tripStartDate) {
  const date = getDateForDay(tripStartDate, day.dayNumber);
  const folderName = `Day ${day.dayNumber} - ${formatShortDate(date)}`;

  const placemarks = day.activities.map(activity => {
    if (!activity.lat || !activity.lng) {
      console.warn(`Missing lat/lng for activity: ${activity.name}. Skipping.`);
      return '';
    }
    const styleId = activity.type || 'default';
    const label = activity.time ? `${activity.name} (${activity.time})` : activity.name;
    return `
    <Placemark>
      <name>${escapeXml(label)}</name>
      <styleUrl>#${styleId}</styleUrl>
      <Point>
        <coordinates>${activity.lng},${activity.lat},0</coordinates>
      </Point>
    </Placemark>`;
  }).join('\n');

  return `
  <Folder>
    <name>${escapeXml(folderName)}</name>
    ${placemarks}
  </Folder>`;
}

function buildHotelPlacemark(hotel) {
  if (!hotel.lat || !hotel.lng) return '';
  return `
  <Placemark>
    <name>${escapeXml(hotel.name)} (Your Hotel)</name>
    <styleUrl>#hotel</styleUrl>
    <Point>
      <coordinates>${hotel.lng},${hotel.lat},0</coordinates>
    </Point>
  </Placemark>`;
}

function escapeXml(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function getDateForDay(startDateStr, dayNumber) {
  const start = new Date(startDateStr);
  start.setDate(start.getDate() + dayNumber - 1);
  return start;
}

function formatShortDate(date) {
  return date.toLocaleDateString('en-US', { month: 'long', day: 'numeric' });
}

function formatMonthYear(dateStr) {
  const d = new Date(dateStr);
  return d.toLocaleDateString('en-US', { month: 'long', year: 'numeric' });
}
